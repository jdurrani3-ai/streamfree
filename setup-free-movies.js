const fs = require('fs');
const path = require('path');

const content = [
  "import { NextRequest, NextResponse } from 'next/server';",
  "import fs from 'fs';",
  "import path from 'path';",
  "",
  "interface NormalizedMovie {",
  "  id: string;",
  "  provider: 'tubi' | 'pluto';",
  "  title: string;",
  "  year: number | null;",
  "  runtime: number | null;",
  "  posterUrl: string | null;",
  "  watchUrl: string;",
  "  source: 'watchmode' | 'fallback';",
  "  region: 'US';",
  "}",
  "",
  "const movieCache = new Map<string, { data: NormalizedMovie[]; ts: number }>();",
  "let sourceIdCache: { tubi: number | null; pluto: number | null; ts: number } | null = null;",
  "const TTL = 6 * 60 * 60 * 1000;",
  "",
  "async function getSourceIds(apiKey: string): Promise<{ tubi: number | null; pluto: number | null }> {",
  "  if (sourceIdCache && Date.now() - sourceIdCache.ts < TTL) {",
  "    return { tubi: sourceIdCache.tubi, pluto: sourceIdCache.pluto };",
  "  }",
  "  try {",
  "    const res = await fetch(`https://api.watchmode.com/v1/sources/?apiKey=${apiKey}&regions=US`);",
  "    const sources = await res.json();",
  "    let tubiId: number | null = null;",
  "    let plutoId: number | null = null;",
  "    if (Array.isArray(sources)) {",
  "      for (const s of sources) {",
  "        const name = (s.name || '').toLowerCase();",
  "        if (name.includes('tubi') && !tubiId) tubiId = s.id;",
  "        if (name.includes('pluto') && !plutoId) plutoId = s.id;",
  "      }",
  "    }",
  "    sourceIdCache = { tubi: tubiId, pluto: plutoId, ts: Date.now() };",
  "    console.log(`Watchmode source IDs — Tubi: ${tubiId}, Pluto: ${plutoId}`);",
  "    return { tubi: tubiId, pluto: plutoId };",
  "  } catch (err) {",
  "    console.error('Failed to fetch Watchmode sources, using known IDs:', err);",
  "    return { tubi: 73, pluto: 300 };",
  "  }",
  "}",
  "",
  "async function fetchMoviesForProvider(",
  "  apiKey: string,",
  "  sourceId: number,",
  "  provider: 'tubi' | 'pluto'",
  "): Promise<NormalizedMovie[]> {",
  "  const cached = movieCache.get(provider);",
  "  if (cached && Date.now() - cached.ts < TTL) return cached.data;",
  "  try {",
  "    const listUrl = new URL('https://api.watchmode.com/v1/list-titles/');",
  "    listUrl.searchParams.set('apiKey', apiKey);",
  "    listUrl.searchParams.set('source_ids', String(sourceId));",
  "    listUrl.searchParams.set('types', 'movie');",
  "    listUrl.searchParams.set('regions', 'US');",
  "    listUrl.searchParams.set('sort_by', 'popularity_desc');",
  "    listUrl.searchParams.set('page', '1');",
  "    const listRes = await fetch(listUrl.toString());",
  "    if (!listRes.ok) throw new Error(`List titles failed: ${listRes.status}`);",
  "    const listData = await listRes.json();",
  "    const titleIds: number[] = (listData.titles || []).slice(0, 12).map((t: { id: number }) => t.id);",
  "    if (!titleIds.length) throw new Error('No titles returned');",
  "    const detailResults = await Promise.all(",
  "      titleIds.map(async (id) => {",
  "        try {",
  "          const url = `https://api.watchmode.com/v1/title/${id}/?apiKey=${apiKey}&append_to_response=sources&regions=US`;",
  "          const res = await fetch(url);",
  "          if (!res.ok) return null;",
  "          return await res.json();",
  "        } catch { return null; }",
  "      })",
  "    );",
  "    const movies: NormalizedMovie[] = detailResults",
  "      .filter(Boolean)",
  "      .map((d) => {",
  "        const sourceEntry = (d.sources || []).find(",
  "          (s: { source_id: number; region: string; web_url: string }) =>",
  "            s.source_id === sourceId && s.region === 'US' && s.web_url",
  "        );",
  "        if (!sourceEntry) return null;",
  "        return {",
  "          id: String(d.id),",
  "          provider,",
  "          title: d.title || 'Unknown Title',",
  "          year: d.year || null,",
  "          runtime: d.runtime_minutes || null,",
  "          posterUrl: d.poster || d.backdrop || null,",
  "          watchUrl: sourceEntry.web_url,",
  "          source: 'watchmode' as const,",
  "          region: 'US' as const,",
  "        };",
  "      })",
  "      .filter(Boolean) as NormalizedMovie[];",
  "    console.log(`Watchmode: fetched ${movies.length} movies for ${provider}`);",
  "    movieCache.set(provider, { data: movies, ts: Date.now() });",
  "    return movies;",
  "  } catch (err) {",
  "    console.error(`Watchmode fetch error for ${provider}:`, err);",
  "    return [];",
  "  }",
  "}",
  "",
  "function getFallback(provider?: string): NormalizedMovie[] {",
  "  try {",
  "    const filePath = path.join(process.cwd(), 'data', 'fallback-movies.json');",
  "    const raw = fs.readFileSync(filePath, 'utf8');",
  "    const all: NormalizedMovie[] = JSON.parse(raw);",
  "    if (!provider || provider === 'all') return all;",
  "    return all.filter((m) => m.provider === provider);",
  "  } catch { return []; }",
  "}",
  "",
  "export async function GET(request: NextRequest) {",
  "  const provider = request.nextUrl.searchParams.get('provider') || 'all';",
  "  const apiKey = process.env.WATCHMODE_API_KEY;",
  "  if (!apiKey) {",
  "    console.error('WATCHMODE_API_KEY is not set — using fallback');",
  "    return NextResponse.json(getFallback(provider));",
  "  }",
  "  try {",
  "    const { tubi: tubiId, pluto: plutoId } = await getSourceIds(apiKey);",
  "    const results: NormalizedMovie[] = [];",
  "    if ((provider === 'tubi' || provider === 'all') && tubiId) {",
  "      const movies = await fetchMoviesForProvider(apiKey, tubiId, 'tubi');",
  "      results.push(...movies);",
  "    }",
  "    if ((provider === 'pluto' || provider === 'all') && plutoId) {",
  "      const movies = await fetchMoviesForProvider(apiKey, plutoId, 'pluto');",
  "      results.push(...movies);",
  "    }",
  "    if (!results.length) {",
  "      console.log('Watchmode returned no results — using fallback');",
  "      return NextResponse.json(getFallback(provider));",
  "    }",
  "    return NextResponse.json(results);",
  "  } catch (err) {",
  "    console.error('free-movies route error:', err);",
  "    return NextResponse.json(getFallback(provider));",
  "  }",
  "}"
].join('\n');

const targetPath = path.join(process.cwd(), 'app', 'api', 'free-movies', 'route.ts');
fs.mkdirSync(path.dirname(targetPath), { recursive: true });
fs.writeFileSync(targetPath, content, 'utf8');
console.log('✅ app/api/free-movies/route.ts written successfully!');
